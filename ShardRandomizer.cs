        //This function expects a list of enemies, the string array that contains the json to edit, and a the spoiler log. possibly make spoiler an optional overload.
        public static void WriteShardsToDataTable(List<EnemyDropTable> listofenemies , string[]datatable, List<string> spoilerlog)
        {
            //for each enemy in the list
            for (int i = 0; i < listofenemies.Count(); i++)
            {
                //write line to json
                int linenumbertowrite = 0;
                string linetowrite = "";
                linetowrite = "      \"ShardId\": \"" + listofenemies[i].ShardId + "\",";
                linenumbertowrite = listofenemies[i].DatatableLineNumber + 2;
                datatable[linenumbertowrite] = linetowrite;
                //write line to spoiler
                spoilerlog.Add( listofenemies[i].FriendlyName + " ***** " + listofenemies[i].ShardId);
            }
        }

        public static List<EnemyDropTable> GenerateEarlyEnemiesList(List<EnemyDropTable> listofenemies)
        {
            List<EnemyDropTable> earlylogicmovement = new List<EnemyDropTable>();
            for (int i = 0; i < listofenemies.Count(); i++)
            {
                if (listofenemies[i].EarlyEnemy == true)
                {
                    earlylogicmovement.Add(listofenemies[i]);
                }
            }

            return earlylogicmovement;
        }
        
